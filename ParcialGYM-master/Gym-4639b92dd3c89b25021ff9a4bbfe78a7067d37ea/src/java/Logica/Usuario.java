package Logica;

/**
 *
 * @author david
 */
public class Usuario implements CalcularIMC{
    private String nombre;
    private int edad;
    private float peso;
    private float estatura;
    private float imc;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public float getPeso() {
        return peso;
    }

    public void setPeso(float peso) {
        this.peso = peso;
    }

    public float getEstatura() {
        return estatura;
    }

    public void setEstatura(float estatura) {
        this.estatura = estatura;
    }

    public float getImc() {
        return imc;
    }

    public void setImc(float imc) {
        this.imc = imc;
    }

    @Override
    public float Operacion(float p, float e) {
        this.imc = p/e*e;
        return this.imc;
    }
    
}
